package org.example
import kotlin.math.*

fun main() {
    val angleInRadians = 0.5 // Example angle in radians, you can use any value you want
    val cos = cos(angleInRadians)
    val sin = sin(angleInRadians)

    println("Cosine of $angleInRadians radians is: $cos")
    println("Sine of $angleInRadians radians is: $sin")
}
